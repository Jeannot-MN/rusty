fn main() {
    // Integers
    let x: i32 = -5;
    let y: u32 = 10;

    println!("Signed: {}", x);
    println!("Unsigned: {}", y);


    // Floating-point numbers
    let pi: f32 = 3.14;
    let e: f64 = 2.71828;

    println!("Pi: {}", pi);
    println!("e: {}", e);


    // Booleans
    let is_rust_fun: bool = true;
    let is_coding_hard: bool = false;

    println!("Is Rust fun? {}", is_rust_fun);
    println!("Is coding hard? {}", is_coding_hard);


    // Characters
    let letter: char = 'A';

    println!("First letter of the alphabet is: {}", letter);
}
